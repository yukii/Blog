		<h3><strong>Armogaste, c'est quoi ?</strong></h3>

		<p>Armogaste est un studio indépendant de jeux vidéo. Créé en 2012, ce studio a développé plusieurs jeux notamment des jeux mobiles tels que Progeny et Catzilla. A sa tête se trouve : Matthias Créviaux, graphiste de formation avec une imagination débordante. En 2019, après 2 ans de développement, Armogaste sort son premier jeu sur PC : A Song in the Void. Le studio intègre Capital Games en mars 2019, au moment il organise l’Indie Game Nation. Ce dernier est un salon dédié à l’univers des jeux vidéo, il a été tenu en mars 2019 et a été co-organisé avec Indie Bear. </p>

